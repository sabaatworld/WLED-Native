HostCliParseResult parseHostCliArgs(int argc, char** argv) {
  HostCliParseResult result;

  for (int i = 1; i < argc; ++i) {
    const std::string arg = argv[i];

    if (arg == "--help") {
      result.options.showHelp = true;
      continue;
    }
    if (arg == "--version") {
      result.options.showVersion = true;
      continue;
    }
    if (arg == "--list-files") {
      result.options.listFiles = true;
      continue;
    }
    if (arg == "--init-presets") {
      result.options.initPresets = true;
      continue;
    }
    if (arg.rfind("--", 0) == 0) {
      if (!requiresValue(arg)) {
        result.ok = false;
        result.error = "Unknown option: " + arg;
        return result;
      }
      if (i + 1 >= argc) {
        result.ok = false;
        result.error = "Missing value for option: " + arg;
        return result;
      }
      if (!assignOptionValue(result.options, arg, argv[++i], result.error)) {
        result.ok = false;
        return result;
      }
      continue;
    }

    result.options.positionalArgs.emplace_back(arg);
  }

  return result;
}
