bool bootstrapHostStorage(const HostStorageLayout& layout, std::string& instanceId, std::string& error) {
  std::error_code filesystemError;
  std::filesystem::create_directories(layout.configDir, filesystemError);
  if (filesystemError) {
    error = "Unable to create configuration directory '" + layout.configDir.string() + "': " + filesystemError.message();
    return false;
  }

  if (!ensureInstanceId(layout.instanceIdFile, instanceId, error)) return false;
  if (!ensureFileExists(layout.cfgFile, kEmptyObjectJson, error)) return false;
  if (!ensureFileExists(layout.wsecFile, kEmptyObjectJson, error)) return false;
  if (!ensureFileExists(layout.presetsFile, kPresetBootstrapJson, error)) return false;
  if (!ensureFileExists(layout.tempPresetFile, kPresetBootstrapJson, error)) return false;

  return true;
}

bool resolveHostStoragePath(const HostStorageLayout& layout, const std::string& logicalPath, std::filesystem::path& resolvedPath, std::string& error) {
  if (logicalPath.empty()) {
    error = "Logical path must not be empty.";
    return false;
  }

  std::string trimmedPath = logicalPath;
  while (!trimmedPath.empty() && trimmedPath.front() == '/') trimmedPath.erase(trimmedPath.begin());
  if (trimmedPath.empty()) {
    error = "Logical path must target a file inside the config root.";
    return false;
  }

  const std::filesystem::path relativePath(trimmedPath);
  if (relativePath.is_absolute()) {
    error = "Absolute path escapes are not allowed: " + logicalPath;
    return false;
  }

  for (const std::filesystem::path& component : relativePath) {
    const std::string token = component.string();
    if (token == "." || token == "..") {
      error = "Path traversal is not allowed: " + logicalPath;
      return false;
    }
  }

  resolvedPath = (layout.configDir / relativePath).lexically_normal();
  const std::string configRoot = layout.configDir.native();
  const std::string candidate = resolvedPath.native();
  const bool insideConfigRoot = candidate == configRoot ||
                                (candidate.size() > configRoot.size() &&
                                 candidate.compare(0, configRoot.size(), configRoot) == 0 &&
                                 candidate[configRoot.size()] == std::filesystem::path::preferred_separator);
  if (!insideConfigRoot) {
    error = "Resolved path escapes the config root: " + logicalPath;
    return false;
  }

  return true;
}

bool readHostStorageFile(const HostStorageLayout& layout, const std::string& logicalPath, std::string& content, std::string& error) {
  std::filesystem::path resolvedPath;
  if (!resolveHostStoragePath(layout, logicalPath, resolvedPath, error)) return false;

  std::ifstream input(resolvedPath, std::ios::binary);
  if (!input.is_open()) {
    error = "Unable to open file for reading: " + resolvedPath.string();
    return false;
  }

  std::ostringstream buffer;
  buffer << input.rdbuf();
  if (!input.good() && !input.eof()) {
    error = "Unable to read file: " + resolvedPath.string();
    return false;
  }

  content = buffer.str();
  return true;
}

bool copyHostStorageFile(const HostStorageLayout& layout, const std::string& sourceLogicalPath, const std::string& destinationLogicalPath, std::string& error) {
  std::filesystem::path sourcePath;
  std::filesystem::path destinationPath;
  if (!resolveHostStoragePath(layout, sourceLogicalPath, sourcePath, error)) return false;
  if (!resolveHostStoragePath(layout, destinationLogicalPath, destinationPath, error)) return false;

  std::error_code filesystemError;
  std::filesystem::copy_file(sourcePath, destinationPath, std::filesystem::copy_options::overwrite_existing, filesystemError);
  if (filesystemError) {
    error = "Unable to copy file from '" + sourcePath.string() + "' to '" + destinationPath.string() + "': " + filesystemError.message();
    return false;
  }

  return true;
}

bool renameHostStorageFile(const HostStorageLayout& layout, const std::string& sourceLogicalPath, const std::string& destinationLogicalPath, std::string& error) {
  std::filesystem::path sourcePath;
  std::filesystem::path destinationPath;
  if (!resolveHostStoragePath(layout, sourceLogicalPath, sourcePath, error)) return false;
  if (!resolveHostStoragePath(layout, destinationLogicalPath, destinationPath, error)) return false;

  std::error_code filesystemError;
  std::filesystem::rename(sourcePath, destinationPath, filesystemError);
  if (filesystemError) {
    error = "Unable to rename file from '" + sourcePath.string() + "' to '" + destinationPath.string() + "': " + filesystemError.message();
    return false;
  }

  return true;
}

bool deleteHostStorageFile(const HostStorageLayout& layout, const std::string& logicalPath, std::string& error) {
  std::filesystem::path resolvedPath;
  if (!resolveHostStoragePath(layout, logicalPath, resolvedPath, error)) return false;

  std::error_code filesystemError;
  const bool removed = std::filesystem::remove(resolvedPath, filesystemError);
  if (filesystemError) {
    error = "Unable to delete file '" + resolvedPath.string() + "': " + filesystemError.message();
    return false;
  }
  if (!removed) {
    error = "Unable to delete file '" + resolvedPath.string() + "': file does not exist.";
    return false;
  }

  return true;
}

bool compareHostStorageFiles(const HostStorageLayout& layout, const std::string& firstLogicalPath, const std::string& secondLogicalPath, bool& identical, std::string& error) {
  std::string firstContent;
  std::string secondContent;
  if (!readHostStorageFile(layout, firstLogicalPath, firstContent, error)) return false;
  if (!readHostStorageFile(layout, secondLogicalPath, secondContent, error)) return false;

  identical = firstContent == secondContent;
  return true;
}

bool validateHostStorageJsonFile(const HostStorageLayout& layout, const std::string& logicalPath, std::string& error) {
  std::string content;
  if (!readHostStorageFile(layout, logicalPath, content, error)) return false;

  if (JsonValidator(content).isValid()) return true;

  error = "Invalid JSON file: " + logicalPath;
  return false;
}

bool createHostStorageBackup(const HostStorageLayout& layout, const std::string& logicalPath, std::string& backupLogicalPath, std::string& error) {
  if (!validateHostStorageJsonFile(layout, logicalPath, error)) return false;

  backupLogicalPath = makeBackupLogicalPath(logicalPath);
  return copyHostStorageFile(layout, logicalPath, backupLogicalPath, error);
}

bool restoreHostStorageBackup(const HostStorageLayout& layout, const std::string& logicalPath, std::string& backupLogicalPath, std::string& error) {
  backupLogicalPath = makeBackupLogicalPath(logicalPath);
  std::filesystem::path backupPath;
  if (!resolveHostStoragePath(layout, backupLogicalPath, backupPath, error)) return false;
  if (!std::filesystem::exists(backupPath)) {
    error = "Backup missing: " + backupLogicalPath;
    return false;
  }
  if (!validateHostStorageJsonFile(layout, backupLogicalPath, error)) return false;
  return copyHostStorageFile(layout, backupLogicalPath, logicalPath, error);
}

bool hostStorageBackupExists(const HostStorageLayout& layout, const std::string& logicalPath, std::string& backupLogicalPath, std::string& error) {
  backupLogicalPath = makeBackupLogicalPath(logicalPath);
  std::filesystem::path backupPath;
  if (!resolveHostStoragePath(layout, backupLogicalPath, backupPath, error)) return false;
  if (std::filesystem::exists(backupPath)) return true;

  error = "Backup missing: " + backupLogicalPath;
  return false;
}

bool listHostStorageFiles(const HostStorageLayout& layout, std::vector<std::string>& fileNames, std::string& error) {
  std::error_code filesystemError;
  if (!std::filesystem::exists(layout.configDir, filesystemError)) {
    error = "Unable to list files inside missing config root: " + layout.configDir.string();
    return false;
  }
  if (filesystemError) {
    error = "Unable to inspect config root '" + layout.configDir.string() + "': " + filesystemError.message();
    return false;
  }

  std::set<std::string> sortedFileNames;
  for (const std::filesystem::directory_entry& entry : std::filesystem::directory_iterator(layout.configDir, filesystemError)) {
    if (filesystemError) {
      error = "Unable to iterate config root '" + layout.configDir.string() + "': " + filesystemError.message();
      return false;
    }
    if (!entry.is_regular_file()) continue;

    const std::string fileName = entry.path().filename().string();
    if (entry.path().extension() != ".json") continue;
    if (fileName.rfind("wsec", 0) == 0 || fileName.rfind("bkp.wsec", 0) == 0) continue;
    sortedFileNames.insert(fileName);
  }

  fileNames.assign(sortedFileNames.begin(), sortedFileNames.end());
  return true;
}
