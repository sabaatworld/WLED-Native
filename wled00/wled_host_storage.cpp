std::filesystem::path resolveHostConfigDirectory(const std::string& cliOverride) {
  if (!cliOverride.empty()) return std::filesystem::path(cliOverride);
  if (const char* configDir = getEnvironmentVariable("WLED_CONFIG_DIR")) return std::filesystem::path(configDir);
  return getDefaultConfigDirectory();
}

HostStorageLayout buildHostStorageLayout(const std::filesystem::path& configDir) {
  const std::filesystem::path normalizedConfigDir = std::filesystem::absolute(configDir).lexically_normal();
  return {
    normalizedConfigDir,
    normalizedConfigDir / kInstanceIdFileName,
    normalizedConfigDir / kCfgJsonFileName,
    normalizedConfigDir / kWsecJsonFileName,
    normalizedConfigDir / kPresetsJsonFileName,
    normalizedConfigDir / kTempPresetJsonFileName,
  };
}

