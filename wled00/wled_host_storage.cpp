const char* getEnvironmentVariable(const char* name) {
  const char* value = std::getenv(name);
  return (value && value[0] != '\0') ? value : nullptr;
}

std::filesystem::path getHomeDirectory() {
  if (const char* home = getEnvironmentVariable("HOME")) return home;
  return std::filesystem::current_path();
}

std::filesystem::path getDefaultConfigDirectory() {
  const std::filesystem::path homeDirectory = getHomeDirectory();

#ifdef __APPLE__
  return homeDirectory / "Library" / "Application Support" / "WLED";
#else
  if (const char* xdgConfigHome = getEnvironmentVariable("XDG_CONFIG_HOME")) {
    return std::filesystem::path(xdgConfigHome) / "wled";
  }
  return homeDirectory / ".config" / "wled";
#endif
}

bool isHexByte(char first, char second) {
  return std::isxdigit(static_cast<unsigned char>(first)) && std::isxdigit(static_cast<unsigned char>(second));
}

bool isValidInstanceId(const std::string& instanceId) {
  if (instanceId.size() != 17U) return false;

  for (size_t i = 0; i < instanceId.size(); ++i) {
    if ((i % 3U) == 2U) {
      if (instanceId[i] != ':') return false;
      continue;
    }
    if (!std::isxdigit(static_cast<unsigned char>(instanceId[i]))) return false;
  }

  for (size_t i = 0; i < instanceId.size(); i += 3U) {
    if (!isHexByte(instanceId[i], instanceId[i + 1U])) return false;
  }

  return true;
}

std::string generateInstanceId() {
  std::random_device randomDevice;
  std::mt19937 generator(randomDevice());
  std::uniform_int_distribution<unsigned> distribution(0U, 255U);
  std::array<uint8_t, 6> bytes{};

  for (uint8_t& value : bytes) value = static_cast<uint8_t>(distribution(generator));

  bytes[0] = static_cast<uint8_t>((bytes[0] | 0x02U) & 0xFEU); // locally administered, unicast

  std::ostringstream output;
  output << std::uppercase << std::hex << std::setfill('0');
  for (size_t i = 0; i < bytes.size(); ++i) {
    if (i) output << ':';
    output << std::setw(2) << static_cast<unsigned>(bytes[i]);
  }
  return output.str();
}

bool readTextFile(const std::filesystem::path& filePath, std::string& value) {
  std::ifstream input(filePath);
  if (!input.is_open()) return false;

  std::getline(input, value);
  return !value.empty();
}

bool writeTextFile(const std::filesystem::path& filePath, const std::string& value, std::string& error) {
  std::ofstream output(filePath, std::ios::trunc);
  if (!output.is_open()) {
    error = "Unable to open file for writing: " + filePath.string();
    return false;
  }

  output << value;
  if (!output.good()) {
    error = "Unable to persist runtime state to: " + filePath.string();
    return false;
  }

  return true;
}

bool ensureFileExists(const std::filesystem::path& filePath, const char* bootstrapContents, std::string& error) {
  if (std::filesystem::exists(filePath)) return true;
  return writeTextFile(filePath, bootstrapContents, error);
}

bool ensureInstanceId(const std::filesystem::path& filePath, std::string& instanceId, std::string& error) {
  if (readTextFile(filePath, instanceId) && isValidInstanceId(instanceId)) return true;

  instanceId = generateInstanceId();
  return writeTextFile(filePath, instanceId + '\n', error);
}

std::string makeBackupLogicalPath(const std::string& logicalPath) {
  std::string trimmedPath = logicalPath;
  while (!trimmedPath.empty() && trimmedPath.front() == '/') trimmedPath.erase(trimmedPath.begin());
  return "bkp." + trimmedPath;
}

